    private void IngresarNumeros(){
        for(int i=0; i<10; i++) {
            String num = JOptionPane.showInputDialog(null, "Ingrese un numero entero: ", "Número "+(i+1), JOptionPane.INFORMATION_MESSAGE);
            if(!num.matches("//d")) {
            
            }
            numeros[i] = Integer.parseInt(num); 
           
        }
        arreglo.setNumero(numeros);
        
        int numMayor = control.ObtenerNumeroMayor(numeros);
        t_mayor.setText(Integer.toString(numMayor));
        int numMenor = control.ObtenerNumeroMenor(numeros);
        t_menor.setText(Integer.toString(numMenor));
        double promedio = control.ObtenerPromedio(numeros);
        t_promedio.setText(Double.toString(promedio));
        t_area.setText(Arrays.toString(numeros));
    }

